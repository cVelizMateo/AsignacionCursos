    private void BtnValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnValidarActionPerformed
        // TODO add your handling code here:
        //validamos que el textfield no estÃ© vacio 
        if(this.TxtIDCatedraticoAsignacion.getText().length()>0){
            emp.setIDEmpleado(parseInt(TxtIDCatedraticoAsignacion.getText()));
            asignacion.setEmplAsignacion(emp);
            aDB.AgregarAsignacion(asignacion);
            
        }else {
            
            JOptionPane.showMessageDialog(null,"Campos de llenado vacios  !!");
        }
    }//GEN-LAST:event_BtnValidarActionPerformed

